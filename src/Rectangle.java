public class Rectangle {
    private int length;
    private int width;

    void setLength(int length) {
        this.length = length;
    }

    int getLength() {
        return length;
    }

    void setWidth(int width) {
        this.width = width;
    }

    int getWidth() {
        return width;
    }

    int getArea() {
        return (length * width);
    }



}
